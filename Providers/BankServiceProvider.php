<?php namespace Modules\Bank\Providers;

use Illuminate\Support\ServiceProvider;

class BankServiceProvider extends ServiceProvider {

	/**
	 * Indicates if loading of the provider is deferred.
	 *
	 * @var bool
	 */
	protected $defer = false;

	/**
	 * Boot the application events.
	 * 
	 * @return void
	 */
	public function boot()
	{
		$this->registerTranslations();
		$this->registerConfig();
	}

	/**
	 * Register the service provider.
	 *
	 * @return void
	 */
	public function register()
	{		
		//
	}

	/**
	 * Register config.
	 * 
	 * @return void
	 */
	protected function registerConfig()
	{
		$this->publishes([
		    __DIR__.'/../Config/config.php' => config_path('bank.php'),
		]);
		$this->mergeConfigFrom(
		    __DIR__.'/../Config/config.php', 'bank'
		);
	}

	/**
	 * Register translations.
	 * 
	 * @return void
	 */
	public function registerTranslations()
	{
		$langPath = base_path('resources/lang/modules/bank');

		if (is_dir($langPath)) {
			$this->loadTranslationsFrom($langPath, 'bank');
		} else {
			$this->loadTranslationsFrom(__DIR__ .'/../Resources/lang', 'bank');
		}
	}

	/**
	 * Get the services provided by the provider.
	 *
	 * @return array
	 */
	public function provides()
	{
		return array();
	}

}
